function generateWeights(sizes: number[]): ITrayDynamicItemList {
    const result: ITrayDynamicItemList = {};

    sizes.forEach((scale) => {
        result['weight' + scale] = {
            content: (
                <g style={{ transform: 'rotate(180deg) scale(' + (0.2 + scale * 0.05) + ')' }}>
                    {weight}
                    <text
                        x="12px"
                        y="80px"
                        style={{
                            stroke: 'none',
                            strokeWidth: '0',
                            fill: heduDrawingColors.white,
                            transform: 'rotate(180deg)',
                            fontWeight: 'bold',
                            fontSize: '40px',
                            textAnchor: 'middle',
                        }}
                    >
                        {scale}
                    </text>
                </g>
            ),
            defaultColor: heduDrawingColors.black,
        };
    });

    return result;
}

